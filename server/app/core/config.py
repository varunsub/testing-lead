from pydantic_settings import BaseSettings


class Settings(BaseSettings):
    DATABASE_URL: str
    SECRET_KEY: str
    ALGORITHM: str = "HS256"
    ACCESS_TOKEN_EXPIRE_MINUTES: int = 30
    SENDGRID_API_KEY: str
    FROM_EMAIL: str
    TO_EMAIL: str
    FRONTEND_URL: str
    BUCKET_NAME: str
    type: str
    project_id: str
    private_key_id: str
    private_key: str
    client_email: str
    client_id: str
    auth_uri: str
    token_uri: str
    auth_provider_x509_cert_url: str
    client_x509_cert_url: str

    class Config:
        env_file = ".env"


settings = Settings()
